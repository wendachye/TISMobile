package com.bizconnectivity.tismobile.Classes;

public class JobDetail {

	String jobID;
	String customerName;
	String productName;
	String tankNo;
	String loadingBayNo;
	String loadingArm;
	String sdsFile;
	String driverID;
	String operatorID;
	String jobDate;

	public String getJobID() {
		return jobID;
	}

	public void setJobID(String jobID) {
		this.jobID = jobID;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getTankNo() {
		return tankNo;
	}

	public void setTankNo(String tankNo) {
		this.tankNo = tankNo;
	}

	public String getLoadingBayNo() {
		return loadingBayNo;
	}

	public void setLoadingBayNo(String loadingBayNo) {
		this.loadingBayNo = loadingBayNo;
	}

	public String getLoadingArm() {
		return loadingArm;
	}

	public void setLoadingArm(String loadingArm) {
		this.loadingArm = loadingArm;
	}

	public String getSdsFile() {
		return sdsFile;
	}

	public void setSdsFile(String sdsFile) {
		this.sdsFile = sdsFile;
	}

	public String getDriverID() {
		return driverID;
	}

	public void setDriverID(String driverID) {
		this.driverID = driverID;
	}

	public String getOperatorID() {
		return operatorID;
	}

	public void setOperatorID(String operatorID) {
		this.operatorID = operatorID;
	}

	public String getJobDate() {
		return jobDate;
	}

	public void setJobDate(String jobDate) {
		this.jobDate = jobDate;
	}
}
